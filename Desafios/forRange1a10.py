for i in range(1, 11):
    print(i)

for i in range(1, 11, 1): 
    print(i)

# O primeiro argumento do range é o start, se não colocar ele, ele começara do 0
# O segundo argumento é o stop, ele é obrigatório
# O terceiro argumento é o step, ele será o número que vai multiplicar os dalista e vai aparecer os números multiplicaveis por ele, se colocar 1 (default) aparece todos, 2 aparece todos os pares, 3 todos os numeros divisiveis por 3 e assim por diante